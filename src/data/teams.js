function getTeams() {
  let teamDetails = {};
  for (let leagueId in teamsData) {
    for (let team of teamsData[leagueId].teams) {
      teamDetails[team.id] = {};
      teamDetails[team.id].name = team.name;
      teamDetails[team.id].shortName = team.shortName;
      teamDetails[team.id].tla = team.tla;
      teamDetails[team.id].crestUrl = team.crestUrl;
    }
  }
  return teamDetails;
}

function teamList(leagueId) {
  const teams = teamsData[leagueId].teams
    .map((team) => {
      return { id: team.id, shortName: team.shortName };
    })
    .sort((team1, team2) => {
      if (team1.shortName < team2.shortName) return -1;
      if (team1.shortName > team2.shortName) return 1;
      return 0;
    });
  return { name: teamsData[leagueId].name, teams };
}

